package org.sing.tcode.entity;

public class LoginInfo {
	
	private Integer lg_id;
	private String lg_account;
	private String lg_info;
	private String lg_ip;
	public Integer getLg_id() {
		return lg_id;
	}
	public void setLg_id(Integer lg_id) {
		this.lg_id = lg_id;
	}
	public String getLg_account() {
		return lg_account;
	}
	public void setLg_account(String lg_account) {
		this.lg_account = lg_account;
	}
	public String getLg_info() {
		return lg_info;
	}
	public void setLg_info(String lg_info) {
		this.lg_info = lg_info;
	}
	public String getLg_ip() {
		return lg_ip;
	}
	public void setLg_ip(String lg_ip) {
		this.lg_ip = lg_ip;
	}
	
	

}

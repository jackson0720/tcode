package org.sing.tcode.entity;

public class Fans {

	private Integer f_id;		//粉丝id
	private String f_focus;		//被关注的人
	private String f_account;	//粉丝账号

	public Integer getF_id() {
		return f_id;
	}
	public void setF_id(Integer f_id) {
		this.f_id = f_id;
	}
	public String getF_focus() {
		return f_focus;
	}
	public void setF_focus(String f_focus) {
		this.f_focus = f_focus;
	}
	public String getF_account() {
		return f_account;
	}
	public void setF_account(String f_account) {
		this.f_account = f_account;
	}

	
	
}
